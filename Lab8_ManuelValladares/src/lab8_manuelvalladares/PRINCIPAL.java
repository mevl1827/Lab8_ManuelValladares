     public void cargar(ArrayList<ACTIVIDADES> P) {
        DefaultTableModel m = (DefaultTableModel) jTable1.getModel();
        for (ACTIVIDADES act : P) {
            if (act.getActividades_sucesoras().isEmpty()) {
                Object[] R = {act.getNombre(), act.getDuracion(), "En Espera."};
                m.addRow(R);
            } else {
                Object[] R = {act.getNombre(), act.getDuracion(), "En Espera."};
                m.addRow(R);
                cargar(act.getActividades_sucesoras());
            }
        }
    }
         public void correr(ArrayList<ACTIVIDADES> P) throws InterruptedException {
        for (ACTIVIDADES act : P) {
            B = new HILO(act, jTable1, pos);
            B.start();
            ++pos;
        }
        B.join();

        for (ACTIVIDADES act : P) {
            for (ACTIVIDADES act2 : act.getActividades_sucesoras()) {
                if (act.getActividades_sucesoras().isEmpty()) {
                    break;
                } else {
                    B = new HILO(act, jTable1, pos);
                    B.start();
                    ++pos;
                }
                B.join();
                correr(act2.getActividades_sucesoras());
            }

        }
}
