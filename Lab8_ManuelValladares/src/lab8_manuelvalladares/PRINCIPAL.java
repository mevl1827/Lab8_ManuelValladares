    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        //String nombre, int inicio_temprano, int duracion, int final_temprano, ACTIVIDADES actividades_predecesoras, ACTIVIDADES actividades_sucesoras, int retraso
        int inicio_temp=1,duracion=Integer.parseInt(tf_dur_act.getText()),final_temprano=inicio_temp+duracion;
        if (cb_act_predecesora.getSelectedItem()==null) {
       PROYECTO temp = (PROYECTO) cb_proyectos_actividades.getSelectedItem();
       DefaultComboBoxModel m=(DefaultComboBoxModel)cb_act_predecesora.getModel();
        ACTIVIDADES acts = new ACTIVIDADES(tf_nom_act.getText(),inicio_temp,duracion,final_temprano,Integer.parseInt(tf_ret_act.getText()),null);
        temp.getActividades().add(acts);
            Activi.add(acts);
        }else{
          PROYECTO temp = (PROYECTO) cb_proyectos_actividades.getSelectedItem();
           ACTIVIDADES acts = new ACTIVIDADES(tf_nom_act.getText(),inicio_temp,duracion,final_temprano,Integer.parseInt(tf_ret_act.getText()), (PROYECTO) cb_proyectos_actividades.getSelectedItem());
          Activi.add(acts);
           temp.getActividades().add(acts);
           int mayor=0;
            for (ACTIVIDADES i : temp.getActividades()) {
                if (i.getInicio_temprano()>mayor) {
                    mayor=i.getFinal_temprano();
                    System.out.println(mayor);
                }
            }
            acts.setInicio_temprano(mayor+1);
            acts.setFinal_temprano(mayor+duracion);
           
            System.out.println(temp.getActividades().get(cb_act_predecesora.getSelectedIndex()).getInicio_temprano());
        }
    }//GEN-LAST:event_jButton3MouseClicked

